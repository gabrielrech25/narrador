function getSpellSlots(classId, level) {
  const cls = CLASSES.find(c => c.id === classId);
  if (!cls || !cls.spellcasting) return null;
  const table = SPELL_SLOTS[cls.spellcasting.type];
  if (!table) return null;
  return table[Math.min(level, 20)] || null;
}

function getMaxSpellLevel(classId, level) {
  const slots = getSpellSlots(classId, level);
  if (!slots) return 0;
  for (let i = 8; i >= 0; i--) if (slots[i] > 0) return i + 1;
  return 0;
}

