function getSpellsForClass(classId) {
  return SPELLS.filter(s => s.classes.includes(classId));
}

function getFeaturesUpToLevel(classId, level) {
  const cls = CLASSES.find(c => c.id === classId);
  if (!cls) return [];
  const result = [];
  for (let l = 1; l <= level; l++) {
    if (cls.features[l]) {
      cls.features[l].forEach(f => result.push({ ...f, atLevel: l }));
    }
  }
  return result;
}

